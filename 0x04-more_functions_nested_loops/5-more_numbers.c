#include "main.h"

/**
 * print_numbers - print numbers 0-14 10times
 * putchar 3 times of usage
 *
 * Return: void
*/

void print_numbers(void)
{
	int num, row, count;

	for (row = 1; row <= 10; row++)
	{
		for (count = 0; count <= 14; count++)
		{
			num = count;
			if (count > 9)
			{
				_putchar(1 + 48);
				num = count % 10;
			}
			_putchar(num + 48);
		}
		_putchar('\n');
	}
}
